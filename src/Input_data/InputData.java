    private void tableSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSiswaMouseClicked
        
        int bar = tableSiswa.getSelectedRow();
        String a = tableSiswa.getValueAt(bar, 0).toString();
        String b = tableSiswa.getValueAt(bar, 1).toString();
        String c = tableSiswa.getValueAt(bar, 2).toString();
        String d = tableSiswa.getValueAt(bar, 3).toString();
        String e = tableSiswa.getValueAt(bar, 4).toString();
        String f = tableSiswa.getValueAt(bar, 5).toString();
        
        txtNIM.setText(a);
        txtNama.setText(b);
        cbAngkatan.setSelectedItem(c);
        cbJurusan.setSelectedItem(d);
        
        if("Laki-Laki".equals(e)){
        rdLaki.setSelected(true);
    } else if ("Perempuan".equals(e)){
        rdPerempuan.setSelected(true);
    }

    if("Aktif".equals(f)){
        rdAktif.setSelected(true);
    } else if ("Tidak Aktif".equals(f)){
        rdTidakAktif.setSelected(true);
        // Menentukan pilihan cbJurusan berdasarkan nilai d
    for (int i = 0; i < cbJurusan.getItemCount(); i++) {
        if (cbJurusan.getItemAt(i).equals(d)) {
            cbJurusan.setSelectedIndex(i);
            break;
        }
    }
    
 }
    }//GEN-LAST:event_tableSiswaMouseClicked


    protected void datatable(){
        Object [] Baris = {"NIM","Nama","Angkatan","Kelas","Jenis Kelamin","Status"};
        mod = new DefaultTableModel(null, Baris);
        String cariitem = Searching.getText();
        
        try {
            String sql = "SELECT * FROM tabel WHERE NIM LIKE '%" + cariitem + "%' OR Nama LIKE '%" + cariitem + "%' ORDER BY NIM ASC";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                mod.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6)
                });
            }
            tableSiswa.setModel(mod);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"data gagal dipanggil"+e);
        }
    }


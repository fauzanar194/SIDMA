    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String NIM = txtNIM.getText().trim();
    String Nama = txtNama.getText().trim();
    
    if (NIM.isEmpty()) {
        JOptionPane.showMessageDialog(null, "NIM harus diisi!");
        return;
    }

    if (Nama.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nama harus diisi!");
        return;
    }

    if (!NIM.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "NIM harus berupa angka!");
        return;
    }

    if (NIM.length() != 11) {
        JOptionPane.showMessageDialog(null, "NIM harus terdiri dari 11 angka!");
        return;
    }

    for (int i = 0; i < tableSiswa.getRowCount(); i++) {
        Object value = tableSiswa.getValueAt(i, 0);
        String existingNIM = (value != null) ? value.toString() : "";
        if (existingNIM.equals(NIM)) {
            JOptionPane.showMessageDialog(null, "NIM sudah digunakan!");
            return;
        }
    }

    String jk = " ";
    if (rdLaki.isSelected()){
        jk = "Laki-Laki";
    } else if (rdPerempuan.isSelected()) {
        jk = "Perempuan";
    }

    String angkatan = cbAngkatan.getSelectedItem().toString();
    String jurusan = cbJurusan.getSelectedItem().toString();
    String status = " ";
    if (rdAktif.isSelected()){
        status = "Aktif";
    } else if (rdTidakAktif.isSelected()){
        status = "Tidak Aktif";
    }

    String sql = "INSERT INTO tabel VALUES (?,?,?,?,?,?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, NIM);
        stat.setString(2, Nama);
        stat.setString(3, angkatan);
        stat.setString(4, jurusan);
        stat.setString(5, jk);
        stat.setString(6, status);

        stat.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
        kosong();
        txtNIM.requestFocus();
    } catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan" + e);
    }
    datatable();

    txtHistory.setText("");
    txtHistory.append("====================================================\n");
    txtHistory.append("                                                      == SIDMA ==\n");
    txtHistory.append("                                    == Sistem Input Data Mahasiswa ==\n");
    txtHistory.append("====================================================\n");

    txtHistory.append("NIM \t: " + NIM + "\n");
    txtHistory.append("Nama \t: " + Nama + "\n");
    txtHistory.append("Angkatan \t: " + angkatan + "\n");
    txtHistory.append("Kelas \t: " + jurusan + "\n");
    txtHistory.append("Jenis Kelamin \t: " + jk + "\n");
    txtHistory.append("Status \t: " + status + "\n");
    }//GEN-LAST:event_btnSimpanActionPerformed

